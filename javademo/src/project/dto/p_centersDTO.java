package project.dto;

public class p_centersDTO {
	private String center_phone;
	private String center_name;
	private String center_id;
	private int location_num;
	
	
	public p_centersDTO(){
		
	}
	

	public String getCenter_phone() {
		return center_phone;
	}

	public void setCenter_phone(String center_phone) {
		this.center_phone = center_phone;
	}

	public String getCenter_name() {
		return center_name;
	}

	public void setCenter_name(String center_name) {
		this.center_name = center_name;
	}

	public String getCenter_id() {
		return center_id;
	}

	public void setCenter_id(String center_id) {
		this.center_id = center_id;
	}

	public int getLocation_num() {
		return location_num;
	}

	public void setLocation_num(int location_num) {
		this.location_num = location_num;
	}
	
	
	

}//end class
